import UIKit

class Friuts {
    
    var id: Int?
    var title: String?
    var name: String?
    var image: UIImage?
    var price: String?
    
    init(id: Int, title: String, name: String, image: UIImage, price: String) {
        self.id = id
        self.title = title
        self.name = title
        self.image = image
        self.price = price
    }
}
